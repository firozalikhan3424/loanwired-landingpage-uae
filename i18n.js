function applyLanguage(lang) {
  const selected = translations[lang] ? lang : 'en';
  localStorage.setItem('loanwired_lang', selected);
  document.documentElement.lang = selected;
  document.body.classList.toggle('rtl', selected === 'ar');

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const text = translations[selected][key];
    if (text) el.textContent = text;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    const text = translations[selected][key];
    if (text) el.setAttribute('placeholder', text);
  });

  document.querySelectorAll('.lang-switch button').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === selected);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const initial = localStorage.getItem('loanwired_lang') || 'en';
  applyLanguage(initial);

  document.querySelectorAll('.lang-switch button').forEach((btn) => {
    btn.addEventListener('click', () => applyLanguage(btn.dataset.lang));
  });
});
